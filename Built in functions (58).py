#Example for functions in python
Num=[10,20,30,40,50]
#len()---->Returns the number of elements in list
print("Length of list is:",len(Num))
#Max()---->Returns the maximum no of elements in list
print("maximum value:",max(Num))
#min()
print("minimum value:",min(Num))
#sum()
print("sum is:",sum(Num))
#sorted()
print("sorted list is:",sorted(Num))
#any()
print(any(Num))
#all()
print(all(Num))