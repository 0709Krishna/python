#Example for membership operator
Num=[11,22,33,44,55,66,77,88,99,100]
print(5 in Num)
print(100 in Num)
str="I Love Python"
print("Python" in str)
print("python" in str)
print("-----------------------------------------------------")
#using not in of membership operator
Num=[11,22,33,44,55,66,77,88,99,100]
print(5 not in Num)
print(100 not in Num)
str="I Love Python"
print("Python" not in str)
print("python" not in str)